/**
 * Tipos de datos
 */

// Número - number
console.log(27);
console.log(27.56);

// Texto - String
console.log("Franklin Camacho");
console.log("Franklin Camacho");
console.log(`Franklin Camacho`);

// Valores lógicos - Booleans
console.log(true);
console.log(false);

// Indefinidos - undefined
console.log(undefined);

// Null
console.log(null);

/**
 *  Tipos de datos estructurales
 */

// Objeto - object
console.log({
  name: "Franklin",
  lastname: "Camacho",
  birthdayDate: null,
  city: ["Barquisimeto", "Caracas", "Maracaibo"],
  adress: { street: "37th" },
});

// Arreglos (lista) - arrays
console.log(["Mexico", "Venezuela", "Ecuador"]);
console.log([21, true, { name: "Franklin" }, [1, 2, 3, 4, 5]]);
